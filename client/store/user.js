import axios from 'axios'
import history from '../history'

/**
 * ACTION TYPES
 */
const GET_USER = 'GET_USER'
const REMOVE_USER = 'REMOVE_USER'
const SET_MY_PRODUCTS = 'SET_MY_PRODUCTS'

/**
 * INITIAL STATE
 */
const defaultUser = {}

/**
 * ACTION CREATORS
 */
const getUser = user => ({type: GET_USER, user})
const removeUser = () => ({type: REMOVE_USER})
const setMyProducts = myProducts => ({type: SET_MY_PRODUCTS, myProducts})

/**
 * THUNK CREATORS
 */

export const fetchMyProducts = () => async dispatch => {
  try {
    const {data} = await axios.get('/api/user/orders')
    const productArr = []

    data.forEach(order => {
      order.products.forEach(product => {
        if (!productArr.includes(product.id)) {
          productArr.push(product.id)
        }
      })
    })

    dispatch(setMyProducts(productArr))
  } catch (err) {
    console.error(err)
  }
}

export const me = () => async dispatch => {
  try {
    const res = await axios.get('/auth/me')
    dispatch(getUser(res.data || defaultUser))
  } catch (err) {
    console.error(err)
  }
}

export const auth = (email, password, method) => async dispatch => {
  let res
  try {
    res = await axios.post(`/auth/${method}`, {email, password})
  } catch (authError) {
    return dispatch(getUser({error: authError}))
  }

  try {
    dispatch(getUser(res.data))
    history.push('/home')
  } catch (dispatchOrHistoryErr) {
    console.error(dispatchOrHistoryErr)
  }
}

export const logout = () => async dispatch => {
  try {
    await axios.post('/auth/logout')
    dispatch(removeUser())
    history.push('/login')
  } catch (err) {
    console.error(err)
  }
}

/**
 * REDUCER
 */
export default function(state = defaultUser, action) {
  switch (action.type) {
    case GET_USER:
      return action.user
    case REMOVE_USER:
      return defaultUser
    case SET_MY_PRODUCTS:
      return {...state, myProducts: action.myProducts}
    default:
      return state
  }
}
